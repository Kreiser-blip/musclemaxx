/**
 * Volume score: reps × weight × (1 + 0.05 × sets)
 * Reps-per-set is the primary strength driver; sets add a smaller bonus.
 */
export function calculateVolumeScore(
  sets: number,
  reps: number,
  weightKg: number,
): number {
  return reps * weightKg * (1 + 0.05 * sets);
}

/** @deprecated Use calculateVolumeScore instead */
export function calculate1RM(weightKg: number, reps: number): number {
  return weightKg * (1 + reps / 30);
}

export function getRankIndex(muscle: MuscleGroup, score: number): number {
  const thresholds = MUSCLE_THRESHOLDS[muscle];
  let rankIdx = -1;
  for (let i = 0; i < thresholds.length; i++) {
    if (score >= thresholds[i]) {
      rankIdx = i;
    } else {
      break;
    }
  }
  return rankIdx;
}

/**
 * Get rank index for a specific exercise using per-exercise thresholds.
 * Falls back to the chest muscle thresholds if exercise not found.
 */
export function getRankIndexForExercise(
  exerciseName: string,
  score: number,
): number {
  const thresholds =
    EXERCISE_THRESHOLDS[exerciseName] ?? MUSCLE_THRESHOLDS[MuscleGroup.chest];
  let rankIdx = -1;
  for (let i = 0; i < thresholds.length; i++) {
    if (score >= thresholds[i]) {
      rankIdx = i;
    } else {
      break;
    }
  }
  return rankIdx;
}

export function getRankName(rankIndex: number): string {
  if (rankIndex < 0) return "Unranked";
  return RANK_NAMES[rankIndex] ?? "Unranked";
}

export function getRankTier(rankIndex: number): string {
  if (rankIndex < 0) return "Unranked";
  return RANK_TIERS[Math.floor(rankIndex / 3)];
}

export function getRankColor(rankIndex: number): string {
  if (rankIndex < 0) return "#444";
  const tier = RANK_TIERS[Math.floor(rankIndex / 3)];
  return RANK_COLORS[tier] ?? "#444";
}

export function getProgressToNextRank(
  muscle: MuscleGroup,
  score: number,
  rankIndex: number,
  exerciseName?: string,
): number {
  const thresholds = exerciseName
    ? (EXERCISE_THRESHOLDS[exerciseName] ?? MUSCLE_THRESHOLDS[muscle])
    : MUSCLE_THRESHOLDS[muscle];
  if (rankIndex < 0) {
    return Math.min(100, (score / thresholds[0]) * 100);
  }
  if (rankIndex >= thresholds.length - 1) return 100;
  const current = thresholds[rankIndex];
  const next = thresholds[rankIndex + 1];
  return Math.min(100, ((score - current) / (next - current)) * 100);
}

export function getNextRankThreshold(
  muscle: MuscleGroup,
  rankIndex: number,
  exerciseName?: string,
): number | null {
  const thresholds = exerciseName
    ? (EXERCISE_THRESHOLDS[exerciseName] ?? MUSCLE_THRESHOLDS[muscle])
    : MUSCLE_THRESHOLDS[muscle];
  if (rankIndex >= thresholds.length - 1) return null;
  return thresholds[rankIndex + 1];
}

